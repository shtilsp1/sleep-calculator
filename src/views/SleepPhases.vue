      </p>

      <h2>Основы физиологии сна</h2>
      <p>
        Сон регулируется сложной системой нейронных и гормональных сигналов. Центральное место в этой системе занимает гипоталамус, который управляет циркадными ритмами – биологическими часами, контролирующими сон и бодрствование. Мелатонин – гормон, вырабатываемый шишковидной железой, играет решающую роль в индикации времени для сна. Его уровень начинает расти вечером, сигнализируя организму о необходимости отдыха, и снижается утром, способствуя пробуждению.
      </p>
      <p>
        Физиологический процесс сна включает изменение активности различных отделов мозга, снижение температуры тела, изменение частоты сердечных сокращений и дыхания. Эти изменения не происходят случайно – они строго регулируются и следуют определённой последовательности, называемой циклом сна.
      </p>

      <h2>Циклы сна: структура и продолжительность</h2>
      <p>
        У взрослого человека типичный цикл сна длится от 90 до 110 минут. За ночь мы проходим через 4–6 таких циклов. Каждый цикл сна состоит из двух основных блоков:
      </p>
      <ul>
        <li><strong>Фаза медленного сна (NREM)</strong> – включает стадии от засыпания до глубокого сна.</li>
        <li><strong>Фаза быстрого сна (REM)</strong> – характеризуется высокой активностью мозга и сновидениями.</li>
      </ul>
      <p>
        В первой половине ночи преобладают стадии глубокого сна, когда организм максимально погружается в восстановительные процессы. Во второй половине ночи доля REM-сна увеличивается, что важно для обработки информации, улучшения памяти и эмоциональной регуляции.
      </p>

      <h2>Детальное рассмотрение фаз сна</h2>

      <h3>1. Фаза засыпания (N1)</h3>
      <p>
        Первая стадия сна, или засыпание, длится обычно от 5 до 15 минут. В этот период человек переходит от состояния бодрствования к состоянию сна. Мышцы начинают расслабляться, замедляется частота сердечных сокращений и дыхания, а мозг производит гипнагогические образы – кратковременные обрывки мыслей или образов. Это очень чувствительный этап, на котором легко проснуться от шума или других внешних стимулов.
      </p>
      <p>
        Несмотря на свою кратковременность, фаза засыпания имеет важное значение, так как она сигнализирует о начале ночного отдыха и подготовке организма к следующим, более глубоким стадиям сна.
      </p>

      <h3>2. Фаза лёгкого сна (N2)</h3>
      <p>
        После переходного состояния наступает фаза лёгкого сна, которая длится около 20–25 минут. Во время этой стадии происходит более глубокое расслабление организма. Мозговая активность замедляется, появляются характерные паттерны в электроэнцефалограмме – сонные веретена и комплексы K. Сонные веретена играют роль в стабилизации сна и защите от пробуждения, а комплексы K – в подавлении ответов на внешние раздражители.
      </p>
      <p>
        Лёгкий сон – это период, когда организм готовится к наступлению фазы глубокого сна. Он обеспечивает необходимое уменьшение активности всех систем организма, снижая метаболизм и потребление энергии.
      </p>

      <h3>3. Фаза глубокого сна (N3)</h3>
      <p>
        Глубокий сон является самой важной стадией для физического восстановления организма. В этот период происходит активная регенерация тканей, выработка гормона роста, укрепление иммунной системы и переработка накопленных за день веществ. Обычно глубокий сон длится от 30 до 50 минут и занимает около 75% всего ночного сна в первой половине ночи.
      </p>
      <p>
        В фазе глубокого сна наблюдается минимальная активность мозга, а пробуждение человека затруднено. Нарушения или недостаток глубокого сна могут приводить к хронической усталости, снижению иммунитета и ухудшению общего самочувствия.
      </p>

      <h3>4. Фаза REM (быстрый сон)</h3>
      <p>
        Фаза быстрого сна (REM) характеризуется высокой активностью мозга, схожей с состоянием бодрствования. Именно в этот период нам снятся сны – зачастую яркие, запоминающиеся и эмоционально насыщенные. REM-сон играет важную роль в консолидации памяти, обработке эмоций и творческом мышлении.
      </p>
      <p>
        REM-фаза начинается обычно через 90–120 минут после засыпания и длится от 10 до 30 минут. К концу ночи продолжительность этой стадии увеличивается, что связано с необходимостью мозга перерабатывать информацию, накопленную за день. В REM-сне наблюдается временное подавление мышечного тонуса, что предотвращает физическую реализацию сновидений.
      </p>

