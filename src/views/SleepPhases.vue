<script lang="ts">
import { defineComponent, onMounted, ref } from 'vue';
import Chart from 'chart.js/auto';
import { useHead } from "@vueuse/head";
import { gsap } from 'gsap';

export default defineComponent({
  name: 'SleepPhases',
  setup() {
    useHead({
      title: 'Somnivance — Фазы сна: что это и как улучшить сон',
      meta: [
        {
          name: 'description',
          content: 'Узнайте, когда лучше ложиться спать, изучите все фазы сна, включая важную фазу быстрого сна, и воспользуйтесь нашим калькулятором сна для оптимального режима отдыха и как улучшить сон с Somnivance.'
        }
      ]
    });
    const phases = ref([
      {
        title: 'Засыпание',
        description: 'Первая стадия, когда вы только начинаете засыпать. Мышцы расслабляются, дыхание замедляется. Длится 5-15 минут.'
      },
      {
        title: 'Лёгкий сон',
        description: 'Сердцебиение и температура тела снижаются. Тело готовится к глубокому сну. Длится около 20 минут.'
      },
      {
        title: 'Глубокий сон',
        description: 'Самая восстановительная фаза. Тело восстанавливается, укрепляется иммунитет. Длится 30-40 минут.'
      },
      {
        title: 'REM (быстрый сон)',
        description: 'Мозг активен, снятся сны. Важна для памяти и обучения. Длится 20-30 минут и увеличивается к утру.'
      }
    ]);

    const title = ref<HTMLElement | null>(null);
    const introText = ref<HTMLElement | null>(null);
    const phasesContainer = ref<HTMLElement | null>(null);
    const phaseCards = ref<HTMLElement[]>([]);
    const indicators = ref<HTMLElement[]>([]);
    const chartSection = ref<HTMLElement | null>(null);
    const chartCanvas = ref<HTMLCanvasElement | null>(null);

    const hoverCard = (index: number) => {
      gsap.to(phaseCards.value[index], {
        scale: 1.05,
        boxShadow: '0 8px 20px rgba(127, 156, 245, 0.3)',
        duration: 0.3,
        ease: 'power2.out'
      });
      gsap.to(indicators.value[index], {
        scale: 1.2,
        rotation: 10,
        duration: 0.3,
        ease: 'elastic.out(1, 0.5)'
      });
    };

    const resetCard = (index: number) => {
      gsap.to(phaseCards.value[index], {
        scale: 1,
        boxShadow: '0 4px 10px rgba(0, 0, 0, 0.2)',
        duration: 0.3,
        ease: 'power2.out'
      });
      gsap.to(indicators.value[index], {
        scale: 1,
        rotation: 0,
        duration: 0.3,
        ease: 'elastic.out(1, 0.5)'
      });
    };

    const renderChart = () => {
      if (!chartCanvas.value) return;
      const ctx = chartCanvas.value.getContext('2d');
      if (!ctx) return;

      new Chart(ctx, {
        type: 'line',
        data: {
          labels: ['0', '10', '20', '30', '50', '70', '90', '110', '130', '150', '170', '190', '210', '230', '250', '270', '290', '310', '330', '350', '370', '390'],
          datasets: [{
            label: 'Фазы сна',
            data: [0, 1, 1, 2, 2, 3, 3, 2, 1, 0, 1, 1, 2, 3, 3, 2, 1, 0, 1, 2, 3, 3],
            borderColor: '#7f9cf5',
            backgroundColor: 'rgba(127, 156, 245, 0.2)',
            fill: true,
            tension: 0.4,
          }]
        },
        options: {
          scales: {
            y: {
              min: 0,
              max: 3,
              ticks: {
                stepSize: 1,
                callback: (value) => ['Засыпание', 'Лёгкий сон', 'Глубокий сон', 'REM'][value]
              }
            },
            x: {
              title: { display: true, text: 'Минуты' }
            }
          },
          plugins: {
            legend: { display: false },
            tooltip: {
              callbacks: {
                label: (context) => {
                  const phase = Math.round(context.raw as number);
                  return ['Засыпание', 'Лёгкий сон', 'Глубокий сон', 'REM'][phase];
                }
              }
            }
          },
          animation: {
            duration: 2000,
            easing: 'easeInOutQuad'
          }
        }
      });
    };

    onMounted(() => {

      if (title.value) {
        gsap.fromTo(
            title.value,
            { opacity: 0, y: -50 },
            { opacity: 1, y: 0, duration: 1, ease: 'power3.out' }
        );
      }

      if (introText.value) {
        gsap.fromTo(
            introText.value,
            { opacity: 0, y: 20 },
            { opacity: 1, y: 0, duration: 1, delay: 0.5, ease: 'power3.out' }
        );
      }

      if (phaseCards.value.length) {
        gsap.fromTo(
            phaseCards.value,
            { opacity: 0, y: 30 },
            { opacity: 1, y: 0, duration: 0.8, stagger: 0.2, ease: 'back.out(1.7)', delay: 1 }
        );
      }

      if (indicators.value.length) {
        gsap.fromTo(
            indicators.value,
            { scale: 0 },
            { scale: 1, duration: 0.5, stagger: 0.2, ease: 'elastic.out(1, 0.5)', delay: 1.2 }
        );
      }

      if (chartSection.value) {
        gsap.fromTo(
            chartSection.value,
            { opacity: 0, y: 50 },
            { opacity: 1, y: 0, duration: 1, ease: 'power3.out', delay: 1.5, onComplete: renderChart }
        );
      }
    });

    return { phases, chartCanvas, title, introText, phasesContainer, phaseCards, indicators, chartSection, hoverCard, resetCard };
  }
});
</script>

